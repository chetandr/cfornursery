    Dapoli is one of the historical places of Maharashtra situated in the midst of the Western Ghats in Ratnagiri District, on the West Coast. The place is well connected by road and train. The city is 220 km away from Mumbai and Pune by road and  the nearest railway station is at Khed (28 km).  
   <br/><br/>
The College of Forestry is the constituent college of Dr. Balasaheb Sawant Konkan Krishi Vidyapeeth, Dapoli, established as Department in 1985 and received the independent status on 1st August, 2005. As part of the extension activity the institute has established a forest nursery in the campus. The mandate of the nursery is to produce quality seedlings of commercial timber and non timber yielding species including bamboo and medicinal plants. The seedlings produced are supplied to the farmers of Konkan region and neighboring districts of Maharashtra. The seeds are collected from superior sources like the candidate trees, seed production areas and seed orchards. The institute has also a mandate for conserving the rare plant species of the Western Ghats, hence, seedlings of RET species are also raised. Annually this nursery produces one lakh seedlings of nearly 100 plant species. This target is increasing year by year due to the increasing demand and various schemes. This nursery is funded by the Mega Seed Project of the Indian Council of Agricultural Research, New Delhi and the University Grants. The nursery is under the Control of Associate Dean, College of Forestry, Dapoli.
